TEST_F(AttestNitroTest, TestSignatureInvalid) {
  uint8_t invalid_bytes[sizeof(valid_bytes)];
  memcpy(invalid_bytes, valid_bytes, sizeof(valid_bytes));
  invalid_bytes[160] ^= 1;  // This flips a single bit in a PCR.
  CoseSign1 cose_sign_1;
  ASSERT_EQ(error::OK, cose_sign_1.ParseFromBytes(invalid_bytes, sizeof(invalid_bytes)));
  AttestationDoc attestation_doc;
  ASSERT_EQ(error::OK, attestation_doc.ParseFromBytes(cose_sign_1.payload.data(), cose_sign_1.payload.size()));
  ASSERT_EQ(error::AttestationNitro_CoseSignatureVerify, Verify(attestation_doc, cose_sign_1, valid_at));
}

